const categories = ['الكل', 'أتعاب', 'أحوال شخصية', 'عقارات', 'وكالات', 'تجارة', 'عمالية', 'أخرى'];

const Contracts = () => {
  const [search, setSearch] = useState('');
  const [category, setCategory] = useState('الكل');
  const [preview, setPreview] = useState(null);

  const filtered = contracts.filter(c => {
    const matchSearch = c.title.includes(search) || c.description.includes(search);
    const matchCat = category === 'الكل' || c.category === category;
    return matchSearch && matchCat;
  });

  const handleDownload = (contract) => {
    const blob = new Blob([contract.content], { type: 'text/plain;charset=utf-8' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = `${contract.title}.txt`;
    a.click();
    URL.revokeObjectURL(url);
  };

  return (
    <div className="space-y-6">
      <div>
        <h1 className="text-3xl font-bold text-white flex items-center gap-3">
          <FileText className="text-amber-400" size={32} />
          العقود الجاهزة
        </h1>
        <p className="text-gray-400 mt-1">نماذج عقود قانونية عراقية جاهزة للتخصيص</p>
      </div>

      {/* فلاتر */}
      <div className="flex flex-col sm:flex-row gap-4">
        <div className="relative flex-1">
          <Search className="absolute right-4 top-1/2 -translate-y-1/2 text-gray-400" size={18} />
          <input
            type="text"
            value={search}
            onChange={e => setSearch(e.target.value)}
            placeholder="ابحث عن عقد..."
            className="input-field pr-12"
          />
        </div>
        <div className="flex gap-2 flex-wrap">
          {categories.map(cat => (
            <button
              key={cat}
              onClick={() => setCategory(cat)}
              className={`px-4 py-2 rounded-lg text-sm font-semibold transition-colors ${
                category === cat
                  ? 'bg-amber-600 text-white'
                  : 'bg-gray-800 text-gray-400 hover:bg-gray-700'
              }`}
            >
              {cat}
            </button>
          ))}
        </div>
      </div>

      {/* قائمة العقود */}
      <div className="grid grid-cols-1 md:grid-cols-2 xl:grid-cols-3 gap-6">
        {filtered.map((contract) => (
          <div key={contract.id} className="card hover:border-amber-600 transition-all group">
            <div className="flex items-start gap-3 mb-4">
              <div className="bg-amber-600 bg-opacity-20 rounded-xl p-3 group-hover:bg-opacity-40 transition-all">
                <FileText size={24} className="text-amber-400" />
              </div>
              <div>
                <h3 className="text-white font-bold text-lg">{contract.title}</h3>
                <span className="badge badge-primary text-xs">{contract.category}</span>
              </div>
            </div>
            <p className="text-gray-400 text-sm mb-4">{contract.description}</p>
            <div className="flex gap-2">
              <button
                onClick={() => setPreview(contract)}
                className="flex-1 flex items-center justify-center gap-2 py-2 bg-gray-700 hover:bg-gray-600 text-white rounded-lg transition-colors text-sm"
              >
                <Eye size={16} />
                معاينة
              </button>
              <button
                onClick={() => handleDownload(contract)}
                className="flex-1 flex items-center justify-center gap-2 py-2 bg-amber-600 hover:bg-amber-700 text-white rounded-lg transition-colors text-sm"
              >
                <Download size={16} />
                تحميل
              </button>
            </div>
          </div>
        ))}
      </div>

      {/* نافذة المعاينة */}
      {preview && (
        <div className="fixed inset-0 bg-black bg-opacity-80 flex items-center justify-center z-50 px-4 py-6">
          <div className="bg-gray-800 rounded-xl w-full max-w-2xl border border-gray-700 shadow-2xl flex flex-col max-h-full">
            <div className="p-5 border-b border-gray-700 flex items-center justify-between">
              <h3 className="text-lg font-bold text-white">{preview.title}</h3>
              <div className="flex gap-2">
                <button
                  onClick={() => handleDownload(preview)}
                  className="flex items-center gap-1 px-3 py-2 bg-amber-600 hover:bg-amber-700 text-white rounded-lg text-sm"
                >
                  <Download size={14} />
                  تحميل
                </button>
                <button onClick={() => setPreview(null)} className="text-gray-400 hover:text-white px-3 py-2">✕</button>
              </div>
            </div>
            <div className="flex-1 overflow-y-auto p-6">
              <pre className="text-gray-200 text-sm whitespace-pre-wrap leading-relaxed font-arabic">
                {preview.content}
              </pre>
            </div>
          </div>
        </div>
      )}
    </div>
  );
};

export default Contracts;
